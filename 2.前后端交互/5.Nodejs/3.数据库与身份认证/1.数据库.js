// 1.1什么是数据库
// 数据库(database)是用来组织、存储和管理数据的仓库。

// 当今世界是一个充满着数据的互联网世界，充斥着大量的数据。数据的来源有很多
// 比如出行记录、消费记录、浏览的网页、发送的消息等等。
// 除了文本类型的数据，图像、音乐、声音也都是数据。
// 为了方便管理互联网世界中的数据，就有了数据库管理系统的概念（简称:数据库)。
// 用户可以对数据库中的数据进行新增、查询、更新、删除等操作。


// 1.2常见的数据库及分类
// 市面上的数据库有很多种，最常见的数据库有如下几个:
// 1.MySQL数据库（目前使用最广泛、流行度最高的开源免费数据库;Community + Enterprise)
// 2.Oracle数据库(收费)
// 3.SQL Server 数据库(收费)
// 4.Mongodb 数据库(Community + Enterprise)
// 其中，MySQL、Oracle、SQL Server属于传统型数据库(又叫做:关系型数据库或SQL数据库)，这三者的设计理念相同，用法比较类似。
// 而Mongodb属于新型数据库(又叫做:非关系型数据库或NoSQL数据库)，它在一定程度上弥补了传统型数据库的缺陷。



