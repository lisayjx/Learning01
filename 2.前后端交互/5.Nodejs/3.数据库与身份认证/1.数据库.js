// 1.什么是SQL
// SQL(英文全称: Structured Query Language)是结构化查询语言，
// 专门用来访问和处理数据库的编程语言。能够让我们以编程的形式，
// 操作数据库里面的数据。
 
// 三个关键点:
// SQL是一门数据库编程语言
// 使用SQL语言编写出来的代码，叫做SQL语句
// SQL,语言只能在关系型数据库中使用（例如MySQL、Oracle、SQL Server)。
// 非关系型数据库（例如Mongodb)，不支持SQL语言
// 2.SQL能做什么
// 从数据库中查询数据
// 向数据库中插入新的数据
// 更新数据库中的数据
// 从数据库删除数据可以创建新数据库
// 可在数据库中创建新表
// 可在数据库中创建存储过程、视图
// ..


